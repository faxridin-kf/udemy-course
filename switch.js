let status = 'okay'
 switch (status){
  case 'okay': 
    console.log('====================================');
    console.log('okay');
    console.log('====================================');
    break;
  case 'not okay':
    console.log('====================================');
    console.log('not okay');
    console.log('====================================');
    break;
  case 'error':
    console.log('====================================');
    console.log('error');
    console.log('===================================='); 
    break;
  default:
   console.log('====================================');
   console.log('default message');
   console.log('====================================');
  
 }